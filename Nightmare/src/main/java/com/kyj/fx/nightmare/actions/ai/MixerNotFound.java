package com.kyj.fx.nightmare.actions.ai;

/**
 * 
 */
public class MixerNotFound extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public MixerNotFound() {
		super();

	}

	public MixerNotFound(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public MixerNotFound(String message, Throwable cause) {
		super(message, cause);

	}

	public MixerNotFound(String message) {
		super(message);

	}

	public MixerNotFound(Throwable cause) {
		super(cause);

	}

}
